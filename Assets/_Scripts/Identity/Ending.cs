namespace RunLight.Identity
{
    /// <summary>
    /// 三種結局走向(企劃書 9.1)。由自我認同值與記憶碎片收集度決定。
    /// </summary>
    public enum Ending
    {
        /// <summary>微光 — 低～中認同值。改變雖小,但已是開始。</summary>
        Glimmer,

        /// <summary>破曉 — 中～高認同值。被看見不再那麼可怕。</summary>
        Daybreak,

        /// <summary>溯光 — 高認同值 + 全收集。說出口的那一刻,選擇了自己。</summary>
        Verso
    }
}
